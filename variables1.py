# assigning multiple values to multiple variables
x, y, z = "Tejas", "Lalit", "Patil"
print(3, x)
print(y)
print(z)
# printing in single line
print(8, x, y, z)

# assigning one value to many variables
a = b = c = "Tejas"
print(13, a)
print(b)
print(c)

# unpacking collection
name = ["Tejas", "Lalit", "Patil"]
m, n, o = name
print(m)
print(n)
print(o)

# to output multiple variables
